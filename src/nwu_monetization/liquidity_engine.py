import json
import logging
from datetime import datetime
from typing import Dict, List
import uuid

logging.basicConfig(level=logging.INFO)
logger = logging.getLogger("NWU-Engine")

class DataLiquidityBond:
    def __init__(self, asset_id: str, data_type: str, volume_gb: float):
        self.bond_id = str(uuid.uuid4())
        self.asset_id = asset_id
        self.data_type = data_type
        self.volume_gb = volume_gb
        self.recombination_count = 0
        self.base_liquidity_score = 0.85
        self.accrued_yield_usd = 0.0
        self.timestamp = datetime.now().isoformat()

    def recombine(self, usage_value_usd: float):
        """Called when this data is reused in a new AI model or dataset"""
        self.recombination_count += 1
        self.base_liquidity_score = min(1.0, self.base_liquidity_score + 0.05)
        # Yield is a function of the value generated by the recombination
        self.accrued_yield_usd += (usage_value_usd * 0.11) # 11% yield rate
        logger.info(f"Bond {self.bond_id[:8]} recombined. Yield: ${self.accrued_yield_usd:.2f}")

class MonitoredMonetizationHub:
    def __init__(self):
        self.bonds: Dict[str, DataLiquidityBond] = {}
        self.daily_logs: List[Dict] = []

    def ingest_data_stream(self, asset_id: str, data_type: str, volume_gb: float) -> str:
        bond = DataLiquidityBond(asset_id, data_type, volume_gb)
        self.bonds[bond.bond_id] = bond
        logger.info(f"Ingested {volume_gb}GB of {data_type}. Issued Bond: {bond.bond_id}")
        return bond.bond_id

    def simulate_market_recombination(self):
        """Simulates external partners buying access to the data"""
        total_yield = 0
        for bond_id, bond in self.bonds.items():
            # Simulate a partner finding $500 of value in this data
            bond.recombine(500.0)
            total_yield += bond.accrued_yield_usd
        
        self.daily_logs.append({
            "timestamp": datetime.now().isoformat(),
            "active_bonds": len(self.bonds),
            "total_system_yield_usd": total_yield
        })
        return total_yield

